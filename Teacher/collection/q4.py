# Find the Largest Number in a List

# Create a list of numbers.

# Use a for loop to find the largest number without using max().

# Print the largest number.


listNumber = [2, 3, 1, 5, 6, 10, 4, 1]
max = listNumber[0]
for num in listNumber :

    if num > max : 
        max = num

print(max)




